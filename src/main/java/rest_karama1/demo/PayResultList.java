package rest_karama1.demo;

public class PayResultList {
    private long total;
    private long taux;
    private long finalsum;

    public PayResultList() {
    }

    public long getTotal() {
        return total;
    }

    public void setTotal(long total) {
        this.total = total;
    }

    public long getTaux() {
        return taux;
    }

    public void setTaux(long taux) {
        this.taux = taux;
    }

    public long getFinalsum() {
        return finalsum;
    }

    public void setFinalsum(long finalsum) {
        this.finalsum = finalsum;
    }
}
